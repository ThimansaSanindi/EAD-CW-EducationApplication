    private void login_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_btnActionPerformed
        // TODO add your handling code here:
           String email=email_txt.getText();
           String Password=pwd.getText();
           String query; //to get the sql query string
 
    // Check the entered credentials
     if(email.equals("abc@gmail.com")&& Password.equals("123"))
                 {
                 stLogin f2 =new stLogin();
                 f2.setVisible(true);
                 this.dispose();
                 }
     else if (email.isEmpty()|| Password.isEmpty()) {
          JOptionPane.showMessageDialog(this, "All fields are required","Login Error",JOptionPane.ERROR_MESSAGE);
          return;
          }
     
        try
        {
           Class.forName("com.mysql.cj.jdbc.Driver");//Loads the MySQL database driver so Java can communicate with the database.
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/registration_db","root",""); //Connects to the database and creates a Statement object to run SQL commands.
            Statement st = conn.createStatement();

            query = "SELECT * FROM login WHERE email = '" + email + "' AND password ='" + Password +"'";
            ResultSet rs=st.executeQuery(query);
                 
         if(rs.next()){
             
             JOptionPane.showMessageDialog(this,"Login successful","success",JOptionPane.INFORMATION_MESSAGE);
             
                this.dispose();
                Dashboard dash= new Dashboard();
                dash.setVisible(true);
            }
        else{
            JOptionPane.showMessageDialog(null,"Entered email or password is incorrect","Login Error",JOptionPane.YES_OPTION);
            }
           conn.close();
    }catch(Exception e){
        System.out.println("Error occured:" +e.getMessage());
    }
  

    
    }//GEN-LAST:event_login_btnActionPerformed

