    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        // TODO add your handling code here:
        String course_name = "";
        String name;
        int age;
        String email;
        int mobile;
        String address;
        String gender = "";

        buttonGroup.add(male_rbtn);
        buttonGroup.add(female_rbtn);
        String query;

        // Connect to the DB
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            String url = "jdbc:mysql://localhost:3306/registration_db";
            String user = "root";
            String pass = "";

            Connection conn = DriverManager.getConnection(url, user, pass);
            Statement st = conn.createStatement();

            // Validate input fields
            if ("".equals(cmbox.getSelectedItem())) {
                JOptionPane.showMessageDialog(new JFrame(), "Select a course", "Error", JOptionPane.ERROR_MESSAGE);
            } else if ("".equals(name_txt.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Enter the full name", "Error", JOptionPane.ERROR_MESSAGE);
            } else if ("".equals(age_txt.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Enter age", "Error", JOptionPane.ERROR_MESSAGE);
            } else if ("".equals(email_txt.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Enter email", "Error", JOptionPane.ERROR_MESSAGE);
            } else if ("".equals(mobile_txt.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Enter mobile number", "Error", JOptionPane.ERROR_MESSAGE);
            } else if ("".equals(add_txt.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Enter address", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (!male_rbtn.isSelected() && !female_rbtn.isSelected()) {
                JOptionPane.showMessageDialog(this, "Select gender", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                // Collect data
                name = name_txt.getText();
                age = Integer.parseInt(age_txt.getText());
                email = email_txt.getText();
                address = add_txt.getText();
                mobile = Integer.parseInt(mobile_txt.getText());
                course_name = (String) cmbox.getSelectedItem();

                if (male_rbtn.isSelected()) {
                    gender = "male";
                } else if (female_rbtn.isSelected()) {
                    gender = "female";
                }

                // Correct SQL syntax
                query = "INSERT INTO stRegistration (course_name, name, age, email, mobile, address, gender) "
                        + "VALUES ('" + course_name + "','" + name + "'," + age + ",'" + email + "'," + mobile + ",'" + address + "','" + gender + "')";

                st.executeUpdate(query);

                // Clear form
                name_txt.setText("");
                age_txt.setText("");
                email_txt.setText("");
                mobile_txt.setText("");
                add_txt.setText("");
                buttonGroup.clearSelection();
                cmbox.setSelectedIndex(-1); // Instead of removeAllItems()

                JOptionPane.showMessageDialog(new JFrame(), "Successfully registered.", "Success", JOptionPane.INFORMATION_MESSAGE);
                conn.close();
            }

        } catch (Exception e) {
            System.out.println("Can't connect to database: " + e.getMessage());
        }
    


        
    }//GEN-LAST:event_submit_btnActionPerformed

