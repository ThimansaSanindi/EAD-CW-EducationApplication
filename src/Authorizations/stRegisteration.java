    private void delete_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_btnActionPerformed
        // TODO add your handling code here:
        
    String name = name_txt.getText();

    // Check if ID is empty
    if ("".equals(name)) {
        JOptionPane.showMessageDialog(this, "Enter Student name to delete", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Confirm delete
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirm", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/registration_db", "root", "");
        Statement st = conn.createStatement();

        String deleteQuery = "DELETE FROM stRegistration WHERE name='" + name + "'";
        int rowsDeleted = st.executeUpdate(deleteQuery);

        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "Record deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            // Clear form fields
      
            name_txt.setText("");
            age_txt.setText("");
            email_txt.setText("");
            mobile_txt.setText("");
            add_txt.setText("");
            buttonGroup.clearSelection();
            cmbox.removeAllItems();
        } else {
            JOptionPane.showMessageDialog(this, "No record found with this Student Name", "Warning", JOptionPane.WARNING_MESSAGE);
        }

        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_delete_btnActionPerformed

    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        // TODO add your handling code here:
        String course_name = (String) cmbox.getSelectedItem();
        String name = name_txt.getText().trim();
        String ageStr = age_txt.getText().trim();
        String mobileStr = mobile_txt.getText().trim();
        String email = email_txt.getText().trim();
        String address = add_txt.getText().trim();
        String gender = "";


    if (name.isEmpty() || ageStr.isEmpty() || mobileStr.isEmpty() || email.isEmpty() || address.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill in all required fields.");
        return;
    }
    else if(!male_rbtn.isSelected() && !female_rbtn.isSelected()) {
        JOptionPane.showMessageDialog(this, "Select gender", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

        // Assign gender
    else if (male_rbtn.isSelected()) {
            gender = "male";
        } else if (female_rbtn.isSelected()) {
            gender = "female";
        }

    int age, mobile;
    try {
        age = Integer.parseInt(ageStr);
        mobile = Integer.parseInt(mobileStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Please enter valid numbers for age and mobile.");
        return;
    }

        try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/registration_db", "root", "");
        Statement st = conn.createStatement();

       String updateQuery = "UPDATE stRegistration SET name='" + name + "', age='" + age + "', email='" +email+"',mobile='" + mobile + "', address='" + address + "', gender='" + gender+ "',course_name ='" + course_name + "' WHERE name='" + name+ "'";

        int rowsAffected = st.executeUpdate(updateQuery);

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Record updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No record found with this name.", "Warning", JOptionPane.WARNING_MESSAGE);
        }

        conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }


        
    }//GEN-LAST:event_update_btnActionPerformed

